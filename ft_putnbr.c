#include "ft_printf.h"

int	ft_putnbr(long nb)

{
	char	c;

	if (nb < 0)
	{
		write (1, "-", 1);
		nb = -nb;
	}
	if (nb >= 10)
		ft_putnbr(nb /10);
	c = (nb % 10) + '0';
	write (1, &c, 1);
}
